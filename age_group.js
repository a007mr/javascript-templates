var age = 5;
var ageGroup;

if (age <= 1) {
  console.log(ageGroup = 'Kittens'); 
}
else {
 if (age > 1 && age <= 3) {
   console.log(ageGroup = 'Young cats');
 }
 if (age > 3 && age <= 7) {
   console.log(ageGroup = 'Cats of middle age');
 }
 if (age > 7) {
   console.log(ageGroup = 'Venerable cats'); 
 }
}